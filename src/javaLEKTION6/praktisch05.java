package javaLEKTION6;

public class praktisch05 {

	public static void main(String[] args) {
		//arithmetische Zuweisungsoperatoren
		
		int num = 20;
		System.out.println(num);
		num *= 5;
		System.out.println(num);
		num += 5;
		System.out.println(num);
		num /= 5;
		System.out.println(num);
		num -= 5;
		System.out.println(num);
		num %= 5;
		System.out.println(num);
	}

}
