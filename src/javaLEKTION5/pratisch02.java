package javaLEKTION5;

public class pratisch02 {

	public static void main(String[] args) {
	  /*  * camelKase-Regel;
          *Alle Buchstaben des ersten Wortes werden klein geschrieben, 
          * der erste Buchstabe des nächsten Wortes wird groß geschrieben und die anderen Buchstaben werden klein geschrieben.
          * 
          * Am Anfang des Variablennamens dürfen keine anderen Zeichen als $ und  _ verwendet werden
          * 
          * 
		*/

		byte    _Alt1     = 127;
		short   _Alt2     = 3588;
		int     $Alt3     = 114558558;
		long    $Alt4     = 1551888815;
		float   nummerEins  = 15.5f;
		double  nummerZwei  = 2.188181888;
	
		boolean ergebnis = true;
		char buchstabe = 'A';
		

		
		System.out.println( _Alt1 );
		System.out.println( _Alt2 );
		System.out.println( $Alt3 );
		System.out.println( $Alt4 );
		System.out.println(nummerEins );
		System.out.println(nummerZwei );
		
		
		System.out.println(  ergebnis);
		System.out.println( buchstabe );
	}

}
